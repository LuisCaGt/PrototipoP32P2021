    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        if (Mantenimiento_Clientes.isNumeric(txt_codigo.getText())) {
            if (txt_codigo.getText().length()!=0&&txt_nombre.getText().length()!=0&&txt_apellido.getText().length()!=0&&
                    txt_nacionalidad.getText().length()!=0&&txt_direccion.getText().length()!=0&&txt_telefono.getText().length()!=0) {
            ClientesDAO huespedesdao = new ClientesDAO();
            Clientes guardarmetodo = new Clientes();
            guardarmetodo.setTarjeta(txt_codigo.getText());
            guardarmetodo.setDpi(txt_nombre.getText());
            guardarmetodo.setNombre(txt_apellido.getText());
            guardarmetodo.setApellido(txt_nacionalidad.getText());
            guardarmetodo.setEmail(txt_direccion.getText());
            guardarmetodo.setTelefono(txt_telefono.getText());
            huespedesdao.insert(guardarmetodo);
            actualizartabla();
            JOptionPane.showMessageDialog(null, "Cliente guardado correctamente");
        }else{
            JOptionPane.showMessageDialog(null, "Existen campos vacios, por favor revise y llene los campos");
        }
        }else{
            JOptionPane.showMessageDialog(null, "El Codigo, unicamente pueden ser números");
        }
        limpiar();
    }//GEN-LAST:event_btn_guardarActionPerformed

