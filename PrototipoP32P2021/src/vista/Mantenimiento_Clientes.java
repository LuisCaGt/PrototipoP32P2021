    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        if (Mantenimiento_Clientes.isNumeric(txt_codigo.getText())) {

            ClientesDAO huespedesdao = new ClientesDAO();
            Clientes buscarmetodo = new Clientes();

            buscarmetodo.setTarjeta(txt_codigo.getText());
            buscarmetodo = huespedesdao.query(buscarmetodo);

            txt_nombre.setText(buscarmetodo.getDpi());
            txt_apellido.setText(buscarmetodo.getNombre());
            txt_nacionalidad.setText(buscarmetodo.getApellido());
            txt_direccion.setText(buscarmetodo.getEmail());
            txt_telefono.setText(buscarmetodo.getTelefono());
        }else{
            JOptionPane.showMessageDialog(null,"El Codigo esta vacio y/o el codigo debe de ser solo números");
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

